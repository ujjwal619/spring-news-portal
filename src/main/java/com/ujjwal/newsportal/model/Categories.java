package com.ujjwal.newsportal.model;

public class Categories {
		int id;
		String title;
		String image;
		
		public int getId() {
			return id;
		}
		public String getTitle() {
			return title;
		}
		public void setTitle(String title) {
			this.title = title;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getImage() {
			return image;
		}
		public void setImage(String image) {
			this.image = image;
		}
}
